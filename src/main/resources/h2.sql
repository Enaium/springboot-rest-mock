DROP TABLE IF EXISTS MEMBER;
CREATE TABLE MEMBER
(
    ID       INT PRIMARY KEY AUTO_INCREMENT,
    USERNAME VARCHAR(20) NOT NULL,
    PASSWORD VARCHAR(20) NOT NULL
);

