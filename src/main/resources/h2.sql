DROP TABLE IF EXISTS POST;
CREATE TABLE POST
(
    ID        INT PRIMARY KEY AUTO_INCREMENT,
    TITLE     VARCHAR(100) NOT NULL,
    CONTENT   TEXT         NOT NULL,
    MEMBER_ID INT          NOT NULL,
    FOREIGN KEY (MEMBER_ID) REFERENCES MEMBER (ID)
);

